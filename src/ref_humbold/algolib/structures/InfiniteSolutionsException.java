package ref_humbold.algolib.structures;

public class InfiniteSolutionsException
    extends Exception
{
    private static final long serialVersionUID = 6587678825370802186L;

    public InfiniteSolutionsException()
    {
        super();
    }

    public InfiniteSolutionsException(String s)
    {
        super(s);
    }

    public InfiniteSolutionsException(Throwable t)
    {
        super(t);
    }

    public InfiniteSolutionsException(String s, Throwable t)
    {
        super(s, t);
    }
}
